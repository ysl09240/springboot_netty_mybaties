public class SmallSingleWave {
    double AoneL;//a段的长度
    double BoneL;//b段的长度
    double ConeL;//c段的长度

    public double getAoneL() {
        return AoneL;
    }

    public void setAoneL(double aoneL) {
        AoneL = aoneL;
    }

    public double getBoneL() {
        return BoneL;
    }

    public void setBoneL(double boneL) {
        BoneL = boneL;
    }

    public double getConeL() {
        return ConeL;
    }

    public void setConeL(double coneL) {
        ConeL = coneL;
    }
}
